    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed

        String login = this.textLogin.getText();
        String s = new String(this.textPassword.getPassword());

        List<Usuario> usuarios = this.udi.procurarPorLogin(login);

        for (int i = 0; i < usuarios.size(); i++) {
            String u = usuarios.get(i).getLogin();
            String p = usuarios.get(i).getSenha();
            
            if (u.equalsIgnoreCase("emmerich") && p.equalsIgnoreCase("32431210")) {
                this.jiu = new JanelaInserirUsuario(em, udi);
                this.jiu.setVisible(true);
            } else {
                if (u.equalsIgnoreCase(login) && p.equalsIgnoreCase(s)) {
                    this.jp = new JanelaPrincipal();
                    this.jp.setVisible(true);
                    this.dispose();
                } else {
                    this.labelAvisoErroLogin.setText("Login ou Senha invalidos!");
                }
            }
        }

    }//GEN-LAST:event_btLoginActionPerformed

