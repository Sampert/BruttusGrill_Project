    private void btGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGravarActionPerformed

        String nome = this.textNome.getText();
        String login = this.textLogin.getText();
        String senha = new String(this.textSenha.getPassword());

        Usuario usuario = new Usuario(nome, login, senha);

        this.udi.save(usuario);
        JOptionPane.showMessageDialog(null, "Usuario salvo com sucesso!");

        this.jcmc = new JanelaConfirmarMaisCadastro(this);
        this.jcmc.setVisible(true);
    }//GEN-LAST:event_btGravarActionPerformed

